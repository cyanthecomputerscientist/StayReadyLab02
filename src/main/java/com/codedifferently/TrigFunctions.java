package com.codedifferently;

public class TrigFunctions {
     double value;

    double sine(double val)
    {
        double sineOf = Math.sin(val);
        return sineOf;
    }

    double cosine(double val)
    {
        double cosineOf = Math.cos(val);
        return cosineOf;
    }
    double tanget(double val)
    {
        double tanOf = Math.tan(val);
        return tanOf;
    }
    double arcsine(double val)
    {
        double arcsin = Math.asin(val);
        return arcsin;
    }

    double arccosine(double val)
    {
        double arccos = Math.acos(val);
        return arccos;
    }

    double arctan(double val)
    {
        double arctan = Math.atan(val);
        return arctan;
    }

}